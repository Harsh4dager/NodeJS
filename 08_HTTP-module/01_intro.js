// Components of Request-Response
/*
it all happen in 3 parts:
1) status line: this is the first line of the request or the response. it contains information about the message, such as the method used, URL, protocol version, and so on.
2) the header: collection of key-value pairs, separated by colon.
message such as the content type, content length, caching information, and so on
3) the body: The Body contains the actual data being sent or received. In the case of requests, it might contain form data or query parameters. In the case of responses, it could be HTML, JSON, XML, or any other data format
 */


